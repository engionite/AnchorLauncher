/// <summary>
/// Runtime UI localization. Bind in XAML as
/// <c>Text="{Binding [key], Source={x:Static loc:Loc.I}}"</c>; calling <see cref="SetLanguage"/>
/// raises the indexer change so every bound string refreshes live (no restart).
///
/// Coverage is the high-visibility chrome — sidebar nav, page headers and the Settings page.
/// Strings not present in a language fall back to English, and unknown keys return the key itself,
/// so adding more keys/screens later is purely additive.
/// </summary>
public sealed class Loc : INotifyPropertyChanged
{
    public static Loc I { get; } = new();

    private Dictionary<string, string> _cur = _en;
    public string Current { get; private set; } = "en";

    public event PropertyChangedEventHandler? PropertyChanged;

    /// <summary>Localized lookup with English fallback, then the raw key.
    /// Null-safe: a read before the first SetLanguage falls back through English.</summary>
    public string this[string key] =>
        (_cur ?? _en).TryGetValue(key, out var v) ? v
        : _en.TryGetValue(key, out var e) ? e
        : key;

    /// <summary>Switch active language by 2-letter code (e.g. "ru"); unknown → English.</summary>
    public void SetLanguage(string? code)
    {
        var c = (code ?? "en").Trim().ToLowerInvariant();
        _cur = _all.TryGetValue(c, out var d) ? d : _en;
        Current = _all.ContainsKey(c) ? c : "en";
        // "Item[]" is WPF's indexer-changed signal — refreshes every [key] binding at once.
        PropertyChanged?.Invoke(this, new PropertyChangedEventArgs("Item[]"));
    }

    /// <summary>Maps a settings display string like "Русский (ru-RU)" to its code "ru".</summary>
    public static string CodeFromDisplay(string? display)
    {
        if (string.IsNullOrWhiteSpace(display)) return "en";
        int a = display.IndexOf('('), b = display.IndexOf(')');
        var inside = (a >= 0 && b > a) ? display.Substring(a + 1, b - a - 1) : display;   // "ru-RU"
        var code = inside.Split('-', '_')[0].Trim().ToLowerInvariant();
        return code.Length == 2 ? code : "en";
    }

